"use client"

import { useState } from "react"
import { useForm, SubmitHandler } from "react-hook-form"
import { zodResolver } from "@hookform/resolvers/zod"
import { z } from 'zod';
import { Mail, Phone, MapPin, Send, Linkedin, Github, Facebook, Instagram, ExternalLink } from "lucide-react"
import { siteConfig } from "@/lib/data"
import { cn } from "@/lib/utils"
import { contactFormSchema } from '@/lib/validations';

type ContactFormInputs = z.infer<typeof contactFormSchema>;

export function ContactSection() {
  const [formStatus, setFormStatus] = useState<{ status: 'idle' | 'loading' | 'success' | 'error', message: string }>({ status: 'idle', message: '' });

  const { register, handleSubmit, formState: { errors }, reset } = useForm<ContactFormInputs>({
    resolver: zodResolver(contactFormSchema),
  });

  const onSubmit: SubmitHandler<ContactFormInputs> = async (data) => {
    setFormStatus({ status: 'loading', message: '' });
    try {
      const response = await fetch('/api/contact', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(data),
      });
      const result = await response.json();
      if (!response.ok) throw new Error(result.message || 'Something went wrong');
      setFormStatus({ status: 'success', message: 'Thank you! Your message has been sent.' });
      reset();
    } catch (error: any) {
      setFormStatus({ status: 'error', message: error.message || 'Failed to send message. Please try again.' });
    }
  }

  return (
    <section id="contact" className="section-padding relative">
      <div className="absolute inset-0 bg-gradient-to-b from-secondary/50 to-transparent pointer-events-none" />
      <div className="max-w-6xl mx-auto px-4 sm:px-6 relative">
        <div className="text-center mb-14">
          <p className="text-sm font-semibold tracking-widest text-accent uppercase mb-2">Contact</p>
          <h2 className="text-3xl sm:text-4xl font-bold text-foreground">
            Get In Touch
          </h2>
          <div className="w-12 h-1 bg-primary rounded-full mx-auto mt-4" />
        </div>

        <div className="grid grid-cols-1 lg:grid-cols-5 gap-8">
          {/* Contact Info */}
          <div className="lg:col-span-2">
            <h3 className="text-xl font-bold text-foreground mb-3">{"Let's Work Together"}</h3>
            <p className="text-sm text-muted-foreground leading-relaxed mb-6">
              Have a project in mind? Feel free to reach out using the form or any of the methods below.
            </p>

            <div className="space-y-4 mb-6">
              <a href={`mailto:${siteConfig.email}`} className="flex items-center gap-3 group no-underline">
                <div className="w-10 h-10 rounded-lg bg-accent/10 flex items-center justify-center group-hover:bg-accent transition-colors">
                  <Mail className="w-4 h-4 text-accent group-hover:text-accent-foreground" />
                </div>
                <div>
                  <p className="text-xs font-bold text-foreground">Email</p>
                  <p className="text-xs text-muted-foreground">{siteConfig.email}</p>
                </div>
              </a>
              <a href={`tel:${siteConfig.phone.replace(/\s/g, "")}`} className="flex items-center gap-3 group no-underline">
                <div className="w-10 h-10 rounded-lg bg-primary/10 flex items-center justify-center group-hover:bg-primary transition-colors">
                  <Phone className="w-4 h-4 text-primary group-hover:text-primary-foreground" />
                </div>
                <div>
                  <p className="text-xs font-bold text-foreground">Phone</p>
                  <p className="text-xs text-muted-foreground">{siteConfig.phone}</p>
                </div>
              </a>
              <div className="flex items-center gap-3">
                <div className="w-10 h-10 rounded-lg bg-accent/10 flex items-center justify-center">
                  <MapPin className="w-4 h-4 text-accent" />
                </div>
                <div>
                  <p className="text-xs font-bold text-foreground">Location</p>
                  <p className="text-xs text-muted-foreground">{siteConfig.location}</p>
                </div>
              </div>
            </div>

            <p className="text-xs font-bold text-foreground mb-2">Follow Me</p>
            <div className="flex gap-2">
              {[
                { Icon: Linkedin, href: siteConfig.social.linkedin, label: "LinkedIn" },
                { Icon: Github, href: siteConfig.social.github, label: "GitHub" },
                { Icon: Facebook, href: siteConfig.social.facebook, label: "Facebook" },
                { Icon: Instagram, href: siteConfig.social.instagram, label: "Instagram" },
                { Icon: ExternalLink, href: siteConfig.social.tiktok, label: "TikTok" },
              ].map(({ Icon, href, label }) => (
                <a
                  key={label}
                  href={href}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="w-9 h-9 rounded-lg bg-secondary flex items-center justify-center text-muted-foreground hover:bg-primary hover:text-primary-foreground transition-all duration-200"
                  aria-label={label}
                >
                  <Icon className="w-4 h-4" />
                </a>
              ))}
            </div>
          </div>

          {/* Form */}
          <form
            onSubmit={handleSubmit(onSubmit)}
            className="lg:col-span-3 bg-card rounded-xl p-6 border border-border/50"
            noValidate
          >
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              <div>
                <label htmlFor="name" className="block text-xs font-bold text-foreground mb-1.5">
                  Your Name
                </label>
                <input
                  {...register("name")}
                  id="name"
                  type="text"
                  className={cn("w-full px-4 py-2.5 rounded-lg bg-background border border-border text-foreground text-sm outline-none focus:border-primary focus:ring-1 focus:ring-primary/20 transition-all", errors.name && "border-destructive focus:border-destructive")}
                  placeholder="Antoney Ouko"
                />
                {errors.name && <p className="text-xs text-destructive mt-1">{errors.name.message}</p>}
              </div>
              <div>
                <label htmlFor="email" className="block text-xs font-bold text-foreground mb-1.5">
                  Your Email
                </label>
                <input
                  {...register("email")}
                  id="email"
                  type="email"
                  className={cn("w-full px-4 py-2.5 rounded-lg bg-background border border-border text-foreground text-sm outline-none focus:border-primary focus:ring-1 focus:ring-primary/20 transition-all", errors.email && "border-destructive focus:border-destructive")}
                  placeholder="aouko178@gmail.com"
                />
                {errors.email && <p className="text-xs text-destructive mt-1">{errors.email.message}</p>}
              </div>
            </div>
            <div className="mt-4">
              <label htmlFor="subject" className="block text-xs font-bold text-foreground mb-1.5">
                Subject
              </label>
              <input
                {...register("subject")}
                id="subject"
                type="text"
                className={cn("w-full px-4 py-2.5 rounded-lg bg-background border border-border text-foreground text-sm outline-none focus:border-primary focus:ring-1 focus:ring-primary/20 transition-all", errors.subject && "border-destructive focus:border-destructive")}
                placeholder="Project Inquiry"
              />
              {errors.subject && <p className="text-xs text-destructive mt-1">{errors.subject.message}</p>}
            </div>
            <div className="mt-4">
              <label htmlFor="message" className="block text-xs font-bold text-foreground mb-1.5">
                Message
              </label>
              <textarea
                {...register("message")}
                id="message"
                rows={4}
                className={cn("w-full px-4 py-2.5 rounded-lg bg-background border border-border text-foreground text-sm outline-none focus:border-primary focus:ring-1 focus:ring-primary/20 transition-all resize-y", errors.message && "border-destructive focus:border-destructive")}
                placeholder="Tell me about your project..."
              />
              {errors.message && <p className="text-xs text-destructive mt-1">{errors.message.message}</p>}
            </div>
            <button
              type="submit"
              disabled={formStatus.status === 'loading'}
              className="mt-4 w-full inline-flex items-center justify-center gap-2 px-6 py-3 rounded-lg bg-primary text-primary-foreground font-semibold text-sm cursor-pointer hover:bg-primary/90 hover:shadow-[0_4px_14px_rgba(30,58,138,0.3)] transition-all duration-200 disabled:opacity-50 disabled:cursor-not-allowed"
            >
              <Send className="w-4 h-4" />
              {formStatus.status === 'loading' ? 'Sending...' : 'Send Message'}
            </button>
            {formStatus.message && (
              <p className={cn("mt-3 text-center text-xs font-semibold",
                formStatus.status === 'success' && 'text-green-600',
                formStatus.status === 'error' && 'text-destructive'
              )}>
                {formStatus.message}
              </p>
            )}
          </form>
        </div>
      </div>
    </section>
  )
}
